# what are variables?
# named containers that can store values

# rules for naming a variable
# must be composed of upper-case or lower-case letters, digits, and the character _ (underscore)
# name must begin with a letter
# underscore character is a letter
# upper and lower case letters are treated different
# name of the variable must not be any of python's reserved words

# pep 8 -- style guide for python code
# 1. variable names should be lowercase, with words seperated by underscores to improve readability (ex: var, my_variable)
# 2. function names follow the same convention as variable names
# 3. possible to use mixed case, but only in contexts where that's already the prevailing style, to retain backwards compatibility

